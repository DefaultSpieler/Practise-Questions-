--197 Rising Temperature
SELECT TODAY.ID 
FROM WEATHER AS TODAY
JOIN WEATHER AS YESTERDAY ON 
    TODAY.RECORDDATE = DATE_ADD(YESTERDAY.RECORDDATE, INTERVAL 1 DAY) AND 
    TODAY.TEMPERATURE > YESTERDAY.TEMPERATURE;
