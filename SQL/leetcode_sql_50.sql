-- 1280. Students and Examinations
SELECT STUD.STUDENT_ID,
       STUD.STUDENT_NAME,
       SUB.SUBJECT_NAME,
       COUNT(EXAM.SUBJECT_NAME) AS ATTENDED_EXAMS
FROM STUDENTS AS STUD
CROSS JOIN SUBJECTS AS SUB
LEFT JOIN EXAMINATIONS AS EXAM ON EXAM.STUDENT_ID = STUD.STUDENT_ID AND 
                                  EXAM.SUBJECT_NAME = SUB.SUBJECT_NAME
GROUP BY STUD.STUDENT_ID, STUD.STUDENT_NAME, SUB.SUBJECT_NAME
ORDER BY STUD.STUDENT_ID, SUB.SUBJECT_NAME;   
      
-- 570. MANAGERS WITH ATLEAST 5 DIRECT REPORTS
SELECT NAME
FROM EMPLOYEE
JOIN (SELECT MANAGERID
      FROM EMPLOYEE
      GROUP BY MANAGERID
      HAVING COUNT(*) >= 5
) M
WHERE EMPLOYEE.ID = M.MANAGERID 

--OR 
SELECT EMP.NAME
FROM EMPLOYEE AS EMP
JOIN EMPLOYEE AS MNG ON MNG.MANAGERID = EMP.ID
GROUP BY EMP.ID, EMP.NAME
HAVING COUNT(*) >= 5;

