FROM SALES AS SALE
JOIN PRODUCT AS PRD ON PRD.PRODUCT_ID = SALE.PRODUCT_ID

-- 1581. Customer Who Visited but Did Not Make Any Transactions
SELECT VISIT.CUSTOMER_ID, COUNT(VISIT.VISIT_ID) AS COUNT_NO_TRANS
FROM VISITS AS VISIT
LEFT JOIN TRANSACTIONS AS TRANS ON TRANS.VISIT_ID = VISIT.VISIT_ID
WHERE TRANS.TRANSACTION_ID IS NULL
GROUP BY CUSTOMER_ID;

-- 197. Rising Temperature
